package com.supervision.wms.app.ui_count.model;

/**
 *
 * @author Thilina Kalum
 */
public class CountModel {
    private String name;
    private Integer count;

    public CountModel() {
    }

    public CountModel(String name, Integer count) {
        this.name = name;
        this.count = count;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getCount() {
        return count;
    }

    public void setCount(Integer count) {
        this.count = count;
    }
    
}
